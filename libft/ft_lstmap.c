#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*iterator;

	(void)f;
	iterator = lst;
	while (iterator)
	{
		iterator = iterator->next;
	}
	return (NULL);
}
